package br.imd.modelo;

public class Cliente {
	private String idClinte;
	private String nome;
	private String data_nasc;
	private String cpf;
	public Cliente(String idClinte, String nome, String data_nasc, String cpf) {
		super();
		this.idClinte = idClinte;
		this.nome = nome;
		this.data_nasc = data_nasc;
		this.cpf = cpf;
	}
	
	

}
